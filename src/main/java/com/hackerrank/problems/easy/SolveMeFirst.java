package com.hackerrank.problems.easy;

public class SolveMeFirst {

    static int solveMeFirst(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {

        int a = 5, b = 6;

        int sum;
        sum = solveMeFirst(a, b);

        System.out.println(sum); // 6
    }

}
